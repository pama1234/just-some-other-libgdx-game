package pama1234.gdx.textmate;

public class StyleConst{
  /**
   * The font style constant indicating a normal weight, non-italic font (value is 0). This
   * constant is also used with <code>ProgressBar</code> to indicate a normal state.
   * <p>
   * <b>Used By:</b>
   * </p>
   * <ul>
   * <li><code>ProgressBar</code></li>
   * </ul>
   */
  public static final int NORMAL=0;
  /**
   * The font style constant indicating a bold weight font (value is 1&lt;&lt;0).
   */
  public static final int BOLD=1<<0;
  /**
   * The font style constant indicating an italic font (value is 1&lt;&lt;1).
   */
  public static final int ITALIC=1<<1;
}
